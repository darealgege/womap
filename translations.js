let currentLanguage = 'en';

function t(key, params = {}) {
    let text = translations[currentLanguage][key] || key;
    for (const [param, value] of Object.entries(params)) {
        text = text.replace(`{${param}}`, value);
    }
    return text;
}

function setLanguage(lang) {
    if (translations[lang]) {
        currentLanguage = lang;
        updateUIText();
        localStorage.setItem('womap_language', lang);

        // Trigger event for other components
        document.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
    }
}

function updateUIText() {
    // Update elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[currentLanguage][key]) {
            if (el.tagName === 'INPUT' && el.getAttribute('placeholder')) {
                el.placeholder = translations[currentLanguage][key];
            } else {
                el.innerHTML = translations[currentLanguage][key];
            }
        }
    });

    // Update specific titles/tooltips
    const tooltips = {
        'toggleInputPanel': 'toggle_panel',
        'useCurrentLocation': 'use_current_location',
        'bookmarkStart': 'bookmarks_title',
        'bookmarkEnd': 'bookmarks_title',
        'mapSettingsBtn': 'settings_btn_title',
        'locationLockBtn': 'location_lock_title',
        'scrollLockBtn': 'scroll_lock_title',
        'clearRouteBtn': 'clear_btn_title'
    };

    for (const [id, key] of Object.entries(tooltips)) {
        const el = document.getElementById(id);
        if (el) el.title = t(key);
    }

    // Update button texts that might be dynamic
    // Update button texts that might be dynamic
    const simBtn = document.getElementById('simulateButton');
    if (simBtn) {
        // Check global simulation state
        if (window.isSimulationRunning) {
            simBtn.textContent = t('stop_btn');
            simBtn.style.backgroundColor = '#dc3545'; // Ensure red color for Stop
        } else {
            simBtn.textContent = t('demo_btn');
            // We don't force blue here because it might be disabled/inactive, 
            // but usually it's blue when ready. Let's leave style management to logic or CSS.
            // But to be safe against the "reset" issue mentioned:
            if (!simBtn.disabled) {
                simBtn.style.backgroundColor = '#007BFF';
            }
        }
    }
}
